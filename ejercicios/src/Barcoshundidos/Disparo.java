package Barcoshundidos;

public class Disparo {
	private int x;
	private int y;

	public Disparo(int ejeX, int ejeY) {
		this.x = ejeX;
		this.y = ejeY;
	}

	/**
	 * @return the x
	 */
	public  int getX() {
		return x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

}
